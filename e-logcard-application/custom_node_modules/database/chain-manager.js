let mongoDb=require("./mongo.js");

exports.saveChainInformations=function(chain,callback){
	let chainCollection = mongoDb.database.collection("chain");
	chainCollection.insert(chain, null, function (error, results) {
		if (error) throw error;
		console.log("La chaine  a bien ete cree");    
		callback(true);
	});	

};

exports.findChain=function(chainName,callback){
	let chainCollection = mongoDb.database.collection("chain");
	chainCollection.find({"name":chainName}).toArray(function(error,results){
		if(error){
			throw error;
		}
		return callback(results.length>0?results[0]:null);
	});
};