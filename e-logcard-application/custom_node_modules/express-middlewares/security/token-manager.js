'use strict';
let jwt = require('jsonwebtoken');  
let expressJwt = require('express-jwt');  
//secret du token
let jwtSecret = '187c6969-df62-4e94-b680-05c7c60af046';  
//one hour
let tokenDuration=3600;
exports.getToken=function(username){
	return jwt.sign({username: username}, jwtSecret, { expiresIn: tokenDuration});
};

exports.jwtMiddleware=expressJwt({ secret: jwtSecret }).unless({ path: [ "/login","/registration"]});
